# 디도스 공격(나쁜 접속)으로 오해받아 차단당할 수 있습니다.
# 이를 방지하기 위해 한 번 질문한 뒤 1초를 기다리도록 설정합니다.
API_REQUEST_INTERVAL = 1.0
