# 나중에 투자 전략을 바꾸고 싶다면 이 파일의 숫자들만 수정하시면 됩니다.

# 1. 초기 자본금 설정 (단위: 원)
# 프로그램이 사용할 총 투자 금액을 1000만원으로 설정합니다.
INITIAL_CAPITAL = 10000000

# 2. 1회 투자 비중 (비율)
# 한 번 주식을 살 때 전체 자본금의 몇 퍼센트를 사용할지 정합니다.
# 0.10은 10%를 의미합니다. (즉, 한 번에 100만원어치만 구매하여 위험을 줄입니다.)
